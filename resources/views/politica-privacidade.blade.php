  <h1>Política de Privacidade</h1>
  
  <p>Esta Política de Privacidade descreve como as informações dos usuários são coletadas, usadas e protegidas ao utilizar nosso aplicativo.</p>

  <h2>1. Informações Coletadas</h2>
  <p>Nosso aplicativo pode coletar as seguintes informações:</p>
  <ul>
    <li>Nome completo (via login com Google)</li>
    <li>Endereço de e-mail (via login com Google)</li>
    <li>ID de usuário do Google</li>
    <li><strong>Localização aproximada do dispositivo</strong> (somente para exibir a posição inicial no mapa)</li>
  </ul>

  <h2>2. Uso das Informações</h2>
  <p>As informações coletadas são utilizadas exclusivamente para:</p>
  <ul>
    <li>Autenticar o usuário</li>
    <li>Personalizar a experiência no aplicativo</li>
    <li><strong>Centralizar o mapa com base na localização atual do usuário (sem armazenar ou compartilhar essa informação)</strong></li>
  </ul>

  <h2>3. Compartilhamento de Informações</h2>
  <p>Não compartilhamos suas informações pessoais com terceiros, exceto quando exigido por lei.</p>

  <h2>4. Armazenamento e Segurança</h2>
  <p>As informações são protegidas com criptografia durante a transmissão e armazenadas com segurança.</p>

  <h2>5. Localização</h2>
  <p>A localização do dispositivo é usada somente para mostrar o mapa na posição atual do usuário. Essa informação <strong>não é armazenada nem compartilhada</strong>.</p>

  <h2>6. Direitos dos Usuários</h2>
  <p>Você pode solicitar a exclusão dos seus dados pessoais a qualquer momento, entrando em contato através do e-mail abaixo.</p>

